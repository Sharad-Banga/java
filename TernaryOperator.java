public class TernaryOperator {
    public static void main(String[] args) {
        int n1=2,n2=5,max;
        System.out.println("first number :"+n1);
        System.out.println("second number :"+n2);

        max = (n1>n2) ? n1 : n2;
        System.out.println("maximum is :"+max);
    }
}
